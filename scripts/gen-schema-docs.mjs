#!/usr/bin/env node
/**
 * Writes Mintlify MDX stubs under apps/docs/schemas/ from @leash/schemas JSON Schema exports.
 * Run after `pnpm --filter @leash/schemas build`.
 */
import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const schemaDir = join(root, 'packages', 'schemas', 'dist', 'schemas');
const outDir = join(root, 'apps', 'docs', 'schemas');

await mkdir(outDir, { recursive: true });

let files;
try {
  files = await readdir(schemaDir);
} catch (e) {
  console.error('Missing JSON schemas. Build @leash/schemas first:', schemaDir);
  process.exit(1);
}

const jsonFiles = files.filter((f) => f.endsWith('.json'));
if (jsonFiles.length === 0) {
  console.error('No *.json in', schemaDir);
  process.exit(1);
}

for (const file of jsonFiles) {
  const slug = file.replace(/\.json$/i, '');
  const raw = await readFile(join(schemaDir, file), 'utf8');
  const title = slug
    .split('-')
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');
  const mdx = `---
title: "${title}"
description: "JSON Schema (\`${file}\`) — generated by scripts/gen-schema-docs.mjs"
---

\`\`\`json
${raw.trim()}
\`\`\`
`;
  await writeFile(join(outDir, `${slug}.mdx`), mdx);
}

console.log(`Wrote ${jsonFiles.length} schema page(s) to ${outDir}`);
